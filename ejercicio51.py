# Haz un programa con la cadena maria que use los metodos upper, lower y capitalize y mostrar.
cadena = "maria"

cadena_upper = cadena.upper()
cadena_lower = cadena.lower()
cadena_capitalize = cadena.capitalize()

print("Original:", cadena)
print("Upper:", cadena_upper)
print("Lower:", cadena_lower)
print("Capitalize:", cadena_capitalize)
