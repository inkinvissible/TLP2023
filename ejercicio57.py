#Define una lista por asignacion que almacene en el primer componente el nombre y en los dos siguientes sus notas. Imprimir el nombre y el promedio de las notas.
# Definir la lista con el nombre y las notas
datos_estudiante = ['Juan', 8.5, 7.9]

# Obtener el nombre del estudiante
nombre_estudiante = datos_estudiante[0]

# Obtener las notas del estudiante
nota1 = datos_estudiante[1]
nota2 = datos_estudiante[2]

# Calcular el promedio de las notas
promedio_notas = (nota1 + nota2) / 2

# Imprimir el nombre y el promedio de las notas
print("Nombre del estudiante:", nombre_estudiante)
print("Promedio de las notas:", promedio_notas)
